class TestForLoops
{
public static void main(String args[])
{
int i,n=13;


for(i=1;i<=10;i++)
{
System.out.println("welcome to java 17 LTS "+i*n);
}

System.out.println("------------------------------");

for(i=1;i<=10;i+=2)
{
System.out.println("welcome to java 17 LTS "+i);
}

System.out.println("------------------------------");

for(i=10;i>=1;i--)
{
System.out.println("welcome to java 17 LTS "+i);
}


}
}