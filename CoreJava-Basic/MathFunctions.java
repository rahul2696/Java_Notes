class MathFunctions
{
public static void main(String args[])
{
int a,b;
a=23;
b=45;

System.out.println(Math.max(a,b));
System.out.println(Math.min(a,b));
System.out.println(Math.sqrt(50));
System.out.println(Math.round(Math.sqrt(50)));
System.out.println(Math.pow(13,3));
System.out.println(Math.log(25));

}
}
